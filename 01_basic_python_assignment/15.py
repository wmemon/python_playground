first = float(input("Enter first value:- "))
second = float(input("Enter second value:- "))
print(f"{first} + {second} = {(first+second):,.2f}")
print(f"{first} - {second} = {(first-second):,.2f}")
print(f"{first} * {second} = {(first*second):,.2f}")
print(f"{first} / {second} = {(first/second):,.2f}")
print(f"{first} // {second} = {(first//second):,.2f}")
print(f"{first} % {second} = {(first%second):,.2f}")
print(f"{first} ** {second} ={(first**second):,.2f}")